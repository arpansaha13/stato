import { Book, StatoConfig } from '../types'

/**
 * Typescript helper for stato.config
 * @param config the config object
 * @returns the same config object
 */
export function defineStatoConfig(config: StatoConfig): StatoConfig {
  return config
}
/**
 * Typescript helper for defining books.
 * @param book the book object
 * @returns the same book object
 */
export function defineBook(book: Book): Book {
  return book
}
